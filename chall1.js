function score(marks){
    if (marks > 79) {
        return "A";
    }else if (marks >= 60 && marks < 79) {
        return "B";
    }else if (marks >= 49 && marks < 60) {
        return "c";
    }else if (marks >= 40 && marks < 49) {
        return "D";
    }else if (marks < 40) {
        return "E";
    }



}
